package learning.demo.mvc.model;

public class Response {
	private Integer id;
	private String message;
	public Integer getId() {
		return id;
	}
	public String getMessage() {
		return message;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public void setMessage(String name) {
		this.message = name;
	}
}
